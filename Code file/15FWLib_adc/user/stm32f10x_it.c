// 串口中断服务函数
void DEBUG_USART_IRQHandler(void)
{
    uint8_t ucTemp;
    if(USART_GetITStatus(DEBUG_USARTx,USART_IT_RXNE)!=RESET)
    {
        ucTemp = USART_ReceiveData(DEBUG_USARTx);
		printf("\nReceive：%c\n",ucTemp);
		if(ucTemp == '1')OLED_DisPlay_On();
		else OLED_DisPlay_Off();
    }
}

